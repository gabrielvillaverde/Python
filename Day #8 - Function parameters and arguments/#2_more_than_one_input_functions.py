# Function with more than one input
def greet(name, location):
    print(f"Hello {name}!")
    print(f"What is it like in {location}?")

greet("Rocio", "Argentina")

# Function with more than one input and key arguments
def greet_2(name, location):
    print(f"Hello {name}!")
    print(f"What is it like in {location}?")

greet_2(name="Gabriel", location="Buenos Aires")
